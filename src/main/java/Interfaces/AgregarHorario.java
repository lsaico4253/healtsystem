    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String inicio=txti.getText();
        String salida=txts.getText();
        if(inicio.matches("^([01]?[0-9]|2[0-3]):[0-5][0-9](:[0-5][0-9])?$") && salida.matches("^([01]?[0-9]|2[0-3]):[0-5][0-9](:[0-5][0-9])?$")){
            Connection conn = null;
                PreparedStatement stmt = null;
                String SQL_INSERT="INSERT INTO public.horarios(horainicio, horafin) VALUES ('"+inicio+"', '"+salida+"');";
                try {
                    conn = getConnection();
                    stmt = conn.prepareStatement(SQL_INSERT);
                    stmt.executeUpdate();
                } catch (SQLException ex) {
                    ex.printStackTrace(System.out);
                }
                finally{
                    try {
                        close(stmt);
                        close(conn);
                    } catch (SQLException ex) {
                        ex.printStackTrace(System.out);
                    }
                }
        }else{
            JOptionPane.showMessageDialog(null, "El formato de hora debe ser HH:MM");
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

