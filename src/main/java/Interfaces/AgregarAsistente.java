            miasistente.setCedula(Cedula);
            miasistente.setNombre(Nombre);
            miasistente.setSegundo_nombre(Segundonombre);
            miasistente.setApellido(Apellido);
            miasistente.setSegundo_apellido(Segundoapellido);
            miasistente.setEdad(Edad);
            miasistente.setGenero(genero);
            miasistente.setTelefono(telefono1);
            miasistente.setTelefono2(telefono2);
            miasistente.setDireccion(Direccion);

            String SQL_UPDATE="UPDATE pacientes SET cedula=?, primernombre=?, segundonombre=?, primerapellido=?, segundoapellido=?, edad=?, direccion=?, genero=?, telefono1=?, telefono2=? WHERE id="+id+";";

            Connection conn = null;
            PreparedStatement stmt = null;
            try {
                conn = getConnection();
                stmt = conn.prepareStatement(SQL_UPDATE);
                stmt.setString(1, miasistente.getCedula());
                stmt.setString(2, miasistente.getNombre());
                stmt.setString(3, miasistente.getSegundo_nombre());
                stmt.setString(4, miasistente.getApellido());
                stmt.setString(5, miasistente.getSegundo_apellido());
                stmt.setString(6, miasistente.getEdad());
                stmt.setString(7, miasistente.getDireccion());
                stmt.setString(8, miasistente.getGenero());
                stmt.setString(9, miasistente.getTelefono());
                stmt.setString(10, miasistente.getTelefono2());
                stmt.executeUpdate();
            } catch (SQLException ex) {
                ex.printStackTrace(System.out);
            }
            finally{
                try {
                    close(stmt);
                    close(conn);
                } catch (SQLException ex) {
                    ex.printStackTrace(System.out);
                }
            }

            txtDireccion.setText("");
            txtPrimerNombre.setText("");
            txtSegundoNombre.setText("");
            txtPrimerApellido.setText("");
            txtSegundoApellido.setText("");
            txtSegundoApellido.setText("");
            txtTelefono1.setText("");
            txtTelefono2.setText("");
            txtDireccion.setText("");
            new Pacientes().setVisible(true);
            this.dispose();

