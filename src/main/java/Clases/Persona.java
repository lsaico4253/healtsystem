package Clases;

/**
 *
 * @author lsaic
 */
public abstract class Persona {
    private int id;
    private int rol;
    private String cedula;

    public Persona(int id, int rol, String cedula) {
        this.id = id;
        this.rol = rol;
        this.cedula = cedula;
    }

    public Persona() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRol() {
        return rol;
    }

    public void setRol(int rol) {
        this.rol = rol;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }
    
}
